for i in range(num_links):
    short = random.choices(allowed_chars, k=6)
    short = "".join(short)
    long = random.choice(websites)
    createdOn = random.choice(datelist)
    analytics = []
    startDate = createdOn
    while startDate < lastday:
        hits_today = random.choice(hits_range)
        seconds_apart = int(86400 / (hits_today + 1))
        for _ in range(hits_today):
            startDate += datetime.timedelta(seconds=seconds_apart)
            analytics.append({
                "ip": "",
                "datetime": int(startDate.timestamp())
            })
        startDate = startDate.replace(hour=0, minute=0, second=0, microsecond=0)
        startDate += datetime.timedelta(days=1)

    analytics = sorted(analytics, key=lambda x: x["datetime"])
    createdOn = int(createdOn.timestamp())

    userlinks.append({
        "short": short,
        "long": long,
        "createdOn": createdOn,
        "analytics": analytics
    })

with open("userlinks", "w") as f:
    f.write(json.dumps(userlinks, indent=4))
